    private int[][] diction = {{-1, 0}, {1, 0}, {0, -1}, {0, 1}};
    private int r;
    private int c;
    Queue<int[]> queue;

    public int shortestBridge(int[][] grid) {
        r = grid.length;
        c = grid[0].length;
        queue = new LinkedList<>();
        int i = 0;
        int j;
        loop1:
        for (; i < r; i++) {
            j = 0;
            for (; j < c; j++) {
                if (grid[i][j] == 1) {
                    dfs(grid, i, j);
                    break loop1;
                }
            }
        }
        return bfs(grid);
    }

    private int bfs(int[][] grid) {
        int len = 0;
        int size;
        while (!queue.isEmpty()) {
            size = queue.size();
            while (--size >= 0) {
                int[] cur = queue.poll();
                for (int i = 0; i < diction.length; i++) {
                    int x = cur[0] + diction[i][0];
                    int y = cur[1] + diction[i][1];
                    if (x >= 0 && x < r && y >= 0 && y < c) {
                        if (grid[x][y] == 1) {
                            return len;
                        } else if (grid[x][y] == 0) {
                            grid[x][y] = 2;
                            queue.add(new int[]{x, y});
                        }
                    }
                }
            }
            len++;
        }
        return len;
    }

    private void dfs(int[][] grid, int i, int j) {
        if (i < 0 || i >= r || j < 0 || j >= c || grid[i][j] != 1) {
            return;
        }
        grid[i][j] = -1;
        queue.add(new int[]{i, j});
        for (int k = 0; k < diction.length; k++) {
            int x = i + diction[k][0];
            int y = j + diction[k][1];
            dfs(grid, x, y);
        }
    }

